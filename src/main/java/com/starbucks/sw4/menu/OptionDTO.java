package com.starbucks.sw4.menu;

public class OptionDTO {

	private String optIndex, hotYn, cupSizeOption;

	public String getOptIndex() {
		return optIndex;
	}

	public void setOptIndex(String optIndex) {
		this.optIndex = optIndex;
	}

	public String getHotYn() {
		return hotYn;
	}

	public void setHotYn(String hotYn) {
		this.hotYn = hotYn;
	}

	public String getCupSizeOption() {
		return cupSizeOption;
	}

	public void setCupSizeOption(String cupSizeOption) {
		this.cupSizeOption = cupSizeOption;
	}
	
	
}
